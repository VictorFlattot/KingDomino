package Model;

/**
 * Enumération des différentes couleurs pour les Rois
 */
public enum Couleur {

	/**
	 * Désigne le roi de couleur Bleu
	 */
	Bleu,

	/**
	 * Désigne le roi de couleur Jaune
	 */
	Jaune,

	/**
	 * Désigne le roi de couleur Rouge
	 */
	Rouge,

	/**
	 * Désigne le roi de couleur Vert
	 */
	Vert;

	/**
     * @return le nom de la couleur
     */
	@Override
	public String toString() {
		return super.toString();
	}
}
